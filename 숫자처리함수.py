# 절댓값 계산 
print(abs(-5)) # 5

# 제곱계산
print(pow(4, 2)) # 4^2 = 4*4 = 16

# 최댓값 출력
print(max(5, 12)) # 12

# 최솟값 출력
print(min(5, 12)) # 5

# 반올림
print(round(3.14)) # 3
print(round(4.99)) # 5

from math import *

# 소수점이하 내림
print(floor(4.99)) # 4

#소수점이하 올림
print(ceil(3.14)) # 4

# 제곱근
print(sqrt(16)) # 4